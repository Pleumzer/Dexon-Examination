import { TestPointsInterface } from "../../../interfaces/ITestPoint"; 
const apiUrl = "http://localhost:8080";

async function GetTestPoint(id: number) {
  const requestOptions = {
    method: "GET",
    headers: {
      "Content-Type": "application/json",
    },
  };

  let res = await fetch(`${apiUrl}/test_points/${id}`, requestOptions)
    .then((response) => response.json())
    .then((res) => {
      if (res.data) {
        return res.data;
      } else {
        return false;
      }
    });

  return res;
}

